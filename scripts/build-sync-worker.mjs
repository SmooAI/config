#!/usr/bin/env node
/**
 * SMOODEV-617 — bundle the synckit worker into a string constant.
 *
 * Why: `@smooai/config/server` needs `.getSync()` to work in bundled
 * consumers (AWS Lambda via esbuild, Vercel edge, etc.). synckit's
 * default API (`createSyncFn(workerPath)`) expects a file path at runtime
 * — which doesn't exist inside an esbuild-bundled Lambda because the
 * worker file gets tree-shaken out. synckit also accepts a `URL` as the
 * worker path, and ships a `dataUrl(code)` helper that turns a string
 * of JS source into a `data:text/javascript,...` URL.
 *
 * This script runs before the main tsup build:
 *   1. esbuild bundles `src/server/sync-worker.ts` into a single ESM
 *      string with every dep inlined (only `node:*` built-ins stay
 *      external). Result: a self-contained worker module.
 *   2. The bundled source is written to
 *      `src/server/sync-worker-source.generated.ts` as a single
 *      exported string constant (`WORKER_SOURCE`).
 *   3. `src/server/index.ts` imports that constant and hands it to
 *      `createSyncFn(dataUrl(WORKER_SOURCE))`. The consumer's bundler
 *      then inlines the string into the Lambda bundle as plain data —
 *      no file lookup at runtime, no tree-shake hazard.
 *
 * Run manually:
 *   node scripts/build-sync-worker.mjs
 *
 * Automated via the `build:sync-worker` npm script, which `build:lib`
 * depends on.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import esbuild from 'esbuild';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const result = await esbuild.build({
    entryPoints: [path.join(repoRoot, 'src/server/sync-worker.ts')],
    bundle: true,
    format: 'esm',
    platform: 'node',
    target: 'node22',
    write: false,
    // Only keep Node builtins external — everything else (synckit, the
    // internal async core + its transitive deps) gets inlined so the
    // resulting source string is self-contained.
    external: ['node:*'],
    // `@/` path alias used by the source tree; map to our src/.
    alias: {
        '@': path.join(repoRoot, 'src'),
    },
    // Transitive CJS deps (fdir, lodash, etc.) use `require(...)`. esbuild
    // rewrites those to a `__require` shim that, in an ESM output, throws
    // "Dynamic require of X is not supported". Polyfill `require` at the
    // top of the bundle via `createRequire(import.meta.url)` so CJS deps
    // keep working when the worker loads this bundle from `/tmp`.
    banner: {
        js: `import { createRequire as __smooaiCreateRequire } from 'node:module';\nconst require = __smooaiCreateRequire(import.meta.url);`,
    },
    minify: false, // readable error traces matter more than size here
    sourcemap: false,
    logLevel: 'error',
});

if (result.outputFiles.length !== 1) {
    throw new Error(`[build-sync-worker] expected 1 output file, got ${result.outputFiles.length}`);
}

const workerSource = result.outputFiles[0].text;

const outputPath = path.join(repoRoot, 'src/server/sync-worker-source.generated.ts');
mkdirSync(path.dirname(outputPath), { recursive: true });

const banner = `/* eslint-disable */
// @ts-nocheck
// AUTO-GENERATED by scripts/build-sync-worker.mjs — do not edit.
// Regenerate with \`pnpm build:sync-worker\` (runs automatically before \`build:lib\`).
//
// Contents: the fully-bundled ESM source of src/server/sync-worker.ts with
// every dep inlined (except Node builtins). Consumed by src/server/index.ts
// and handed to \`synckit.dataUrl()\` so \`createSyncFn()\` works even when
// the SDK is bundled by the consumer (AWS Lambda, Vercel edge, etc.).

`;

writeFileSync(outputPath, `${banner}export const WORKER_SOURCE = ${JSON.stringify(workerSource)};\n`);

console.log(`[build-sync-worker] wrote ${outputPath} (${(workerSource.length / 1024).toFixed(1)} KiB bundled source)`);
